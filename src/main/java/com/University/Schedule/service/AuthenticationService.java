    public AuthenticationResponse register(RegisterRequest request) {
        boolean isValidEmail = emailValidator.test(request.getEmail());
        if (!isValidEmail){
            throw new IllegalStateException("email not valid");
        }

        if (repository.findByEmail(request.getEmail()).isEmpty()) {
            var student = Student.builder()
                    .firstname(request.getFirstname())
                    .lastname(request.getLastname())
                    .email(request.getEmail())
                    .password(passwordEncoder.encode(request.getPassword()))
                    .age(request.getAge())
                    .role(Role.USER)
                    .enabled(false)
                    .build();
            String token = studentService.signUpUser(
                   student
            );

            String link = "http://localhost:8080/student/confirm?token=" + token;
            emailSender.send(
                    request.getEmail(),
                    buildEmail(request.getFirstname(), link));



            return AuthenticationResponse.builder()
                    .token(token)
                    .build();
        } else {
            throw new BadRequestException("Email already used");
        }
    }

    public AuthenticationResponse authenticate(AuthenticationRequest request) {
        Authentication authenticate = authenticationManager.authenticate(
                new UsernamePasswordAuthenticationToken(
                        request.getEmail(),
                        request.getPassword()
                )
        );
        var student = repository.findByEmail(request.getEmail())
            .orElseThrow();

        if (student.getEnabled()) {
            var jwtToken = jwtService.generateToken(student);
            return AuthenticationResponse.builder()
                    .token(jwtToken)
                    .build();
        }
        else throw new IllegalStateException("Email is not verified");

    }

    @Transactional
    public String confirmToken(String token) {
        Token confirmationToken = confirmationTokenService
                .getToken(token)
                .orElseThrow(() ->
                        new IllegalStateException("token not found"));

        if (confirmationToken.getConfirmedAt() != null) {
            throw new IllegalStateException("email already confirmed");
        }

        LocalDateTime expiredAt = confirmationToken.getExpiresAt();

        if (expiredAt.isBefore(LocalDateTime.now())) {
            Student student=confirmationToken.getStudent();
            tokenRepository.delete(confirmationToken);
            repository.delete(student);
            return "expired";
        }

        confirmationTokenService.setConfirmedAt(token);
        enableAppUser(
                confirmationToken.getStudent().getEmail());
        return "confirmed";
    }

    public int enableAppUser(String email) {
        return repository.enableAppUser(email);
    }


